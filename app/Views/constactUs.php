<script>
    $(document).ready(function() {
        $('#mycontactform').submit(function(e) {
            e.preventDefault();
            $('#loader').removeClass('hidden');
            $('#contact_us').addClass('hidden');

            let isValid = true;
            let form = this;
            let formData = new FormData(form);
            const formobject = {};


            formData.forEach((value, key) => {
                formobject[key] = value;
            });


            $.ajax({
                url: "inc/ConatctUsBack.php",
                type: "POST",
                data: JSON.stringify(formobject),
                contentType: "application/json",
                success: function(response) {
                    $("#mycontactform")[0].reset();
                    $('#loader').addClass('hidden');
                    $('#contact_us').removeClass('hidden');


                    if (response.status === 'success') {
                        $('#modelConfirm').removeClass('hidden');
                        setInterval(function() {
                            $('#modelConfirm').addClass('hidden');
                        }, 10000);
                    }
                },
                error: function(xhr, error) {
                    $('#contact_us').prop('disabled', false);
                    alert('An error occurred while submitting the form. Please try again.');
                }
            });

        });
    });
</script>